package Modelo;

public class Numero {

	private int valor;
	private String monto;

	
	public Numero() {
		this.valor = 0;
		this.monto = "";
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int numero) {
		this.valor = numero;
	}

	public String getMonto() {
		return monto;
	}

	public void setMonto(String monto) {
		this.monto = monto;
	}
	
	
	
}
